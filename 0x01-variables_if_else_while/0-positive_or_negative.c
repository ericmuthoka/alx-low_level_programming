#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - if statement entry point
 *
 * Return: always zero
 */

int main(void)
{
	int n;

	printf("Enter any number: ");
	scanf("%d", &n);

	if
		(n > 0);
{
	printf
		("is positive\n");
}
	if
	(n == 0);
{
	printf("Is zero,\n");
}
	if
	(n < 0);
{
	printf("is negative,\n");
}
	return (0);
}
